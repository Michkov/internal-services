package tekton

import (
	"fmt"
	"github.com/redhat-appstudio/internal-services/api/v1alpha1"
	tektonv1beta1 "github.com/tektoncd/pipeline/pkg/apis/pipeline/v1beta1"
	corev1 "k8s.io/api/core/v1"
	v1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"reflect"
	"strings"
)

const (
	// internalRequestLabelPrefix is the prefix of the internal request labels
	internalRequestLabelPrefix = "internal-services.appstudio.openshift.io"
)

var (
	// InternalRequestNameLabel is the label used to specify the name of the internal request associated with the PipelineRun
	InternalRequestNameLabel = fmt.Sprintf("%s/%s", internalRequestLabelPrefix, "name")

	// InternalRequestNamespaceLabel is the label used to specify the namespace of the internal request associated with the PipelineRun
	InternalRequestNamespaceLabel = fmt.Sprintf("%s/%s", internalRequestLabelPrefix, "namespace")
)

// NewPipelineRun creates a PipelineRun for the given InternalRequest. The name will be autogenerated,
// using the name of the request as the prefix. The Pipeline information and namespace for the PipelineRun will be
// extracted from the given InternalServicesConfig.
func NewPipelineRun(internalRequest *v1alpha1.InternalRequest, internalServicesConfig *v1alpha1.InternalServicesConfig) *tektonv1beta1.PipelineRun {
	namespace := internalServicesConfig.Spec.Catalog.Namespace
	if namespace == "" {
		namespace = internalServicesConfig.Namespace
	}

	pipelineRun := &tektonv1beta1.PipelineRun{
		ObjectMeta: v1.ObjectMeta{
			GenerateName: strings.ToLower(reflect.TypeOf(v1alpha1.InternalRequest{}).Name()) + "-",
			Namespace:    namespace,
			Labels: map[string]string{
				InternalRequestNameLabel:      internalRequest.Name,
				InternalRequestNamespaceLabel: internalRequest.Namespace,
			},
		},
		Spec: tektonv1beta1.PipelineRunSpec{
			PipelineRef: getPipelineRef(internalRequest, internalServicesConfig),
		},
	}

	pipelineRun.Spec.Workspaces = append(pipelineRun.Spec.Workspaces, tektonv1beta1.WorkspaceBinding{
		Name: "release-workspace",
		PersistentVolumeClaim: &corev1.PersistentVolumeClaimVolumeSource{
			ClaimName: "release-pvc",
		},
	})

	appendInternalRequestParams(pipelineRun, internalRequest)

	return pipelineRun
}

// appendInternalRequestParams appends the InternalRequest parameters to the PipelineRun passed as an argument.
func appendInternalRequestParams(pipelineRun *tektonv1beta1.PipelineRun, internalRequest *v1alpha1.InternalRequest) {
	for param, value := range internalRequest.Spec.Params {
		pipelineRun.Spec.Params = append(pipelineRun.Spec.Params, tektonv1beta1.Param{
			Name: param,
			Value: tektonv1beta1.ArrayOrString{
				Type:      tektonv1beta1.ParamTypeString,
				StringVal: value,
			},
		})
	}
}

// getPipelineRef returns a PipelineRef generated from the information specified in the given InternalRequest and InternalServicesConfig.
func getPipelineRef(internalRequest *v1alpha1.InternalRequest, internalServicesConfig *v1alpha1.InternalServicesConfig) *tektonv1beta1.PipelineRef {
	if internalServicesConfig.Spec.Catalog.Bundle == "" {
		return &tektonv1beta1.PipelineRef{
			Name: internalRequest.Spec.Request,
		}
	}
	return &tektonv1beta1.PipelineRef{
		ResolverRef: getBundleResolver(internalServicesConfig.Spec.Catalog.Bundle, internalRequest.Spec.Request),
	}
}

// getBundleResolver returns a bundle ResolverRef for the given bundle and pipeline.
func getBundleResolver(bundle, pipeline string) tektonv1beta1.ResolverRef {
	return tektonv1beta1.ResolverRef{
		Resolver: "bundles",
		Params: []tektonv1beta1.Param{
			{
				Name: "bundle",
				Value: tektonv1beta1.ParamValue{
					Type:      tektonv1beta1.ParamTypeString,
					StringVal: bundle,
				},
			},
			{
				Name: "kind",
				Value: tektonv1beta1.ParamValue{
					Type:      tektonv1beta1.ParamTypeString,
					StringVal: "pipeline",
				},
			},
			{
				Name: "name",
				Value: tektonv1beta1.ParamValue{
					Type:      tektonv1beta1.ParamTypeString,
					StringVal: pipeline,
				},
			},
		},
	}
}
